
package operator;

public class OperatorEx1 {
	public static void main(String[] args) {
		
		int num1  = -10;
		int num2 = 20;
		
		System.out.println(+num1);
		System.out.println(+num2);
		
		//-(부호)연산자 : 부호가 바뀜
	
		System.out.println(-num1);
		System.out.println(-num2);
		
		num1 = -num1;
		
		System.out.println(num1);
	}
}
